                        <p class="pb20">人们在欣赏照片时首先会对照片有一个整体印象。特别是在被摄体并不是
那么引人注目的情况下，人们总是无意识地从画面整体寻找出一种均衡感。一张照片究竟是平稳安定还是充满着紧张感，是由构图决定的。被摄体在画面中的位置非
常重要，可以说决定了它的位置，就决定了构图的一半。如果学习构图的基础，首先应该明确被摄体在画面中的位置。一般的构图方式是将被摄体置于画面中央的
"牛眼构图"，这是常用的基础构图方式。还有将主被摄体置于对角线上以引导视线的"对角线构图"，有意表现出道路、河流等S形被摄体动感的"S形构图"，
以及将画面纵横三等分，在等分线交叉点处配置被摄体的"九宫格构图"等。究竟应该使用哪种构图要视被摄体情况而定，没有哪一种场景就一定要使用特定构图的
说法。根据被摄体选择构图方式是比较理想的，可以说了解多少构图方式决定了照片表现的优劣。推荐大家平时边研究构图边多看照片，然后多练习拍摄，才能提高
