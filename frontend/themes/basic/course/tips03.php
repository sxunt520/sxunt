光）模式，尽可能利用相机的自动功能。P（程序自动曝光）模式下，相机会根据场景自动设置合适的光圈与快门速度的组合，拍摄者更易集中精力找寻被摄体。而
且把ISO感光度设为自动的话，就能适应从昏暗的小巷到明亮场所等多种场合的拍摄。当然，也可以自行变更ISO感光度进行拍摄，但不要忘记在拍摄完成后更
改回来。从集中精力拍摄这一点来看，将ISO感光度设置为自动的话，能体验更多拍摄乐趣。</p>
