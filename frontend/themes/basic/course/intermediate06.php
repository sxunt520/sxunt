                        <h5 class="pb10">分别进行对焦和构图</h5>
                        <p class="size13 pb20">对焦锁定是使用单次自动对焦模式进行自动对焦拍摄的一
种拍摄技巧。在单次自动对焦模式下，首先确定合焦位置，然后自由移动相机，重新调整构图后进行拍摄。采用这种方法时，通常仅使用众多自动对焦点中的中央对
焦点，其特征是将对焦操作与构图调整分别进行。想要合焦于没有对焦点的位置，或想边微调构图边快速连拍等情况下利用此功能方便。由于对焦锁定需要在暂时固
定焦点后调整画面（相机），如果相机调整角度过大，特别是上下移动的时候，则可能因相机移动而导致合焦位置偏移。因此需要注意调节构图时，保持相机与被摄
体之间的距离一定，这一点非常重要。</p>
