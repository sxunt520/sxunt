                <h5 class="pb10">看上去赏心悦目的亮度就是照片的恰当曝光</h5>
                <p class="pb10">通常所说的恰当曝光是指采用合适的光量进行拍摄，获得视觉效果良好的亮度。恰当曝光的
标准比较模糊，哪种亮度最好，实际上与拍摄者的拍摄意图有着非常密切的联系。但是在拍摄者并未有意识地使画面较明亮（或较暗）的情况下，恰当曝光通常会自
然而然地落在一定亮度范围内。亮度大幅超出该范围时被称为"曝光过度（过曝、过亮）"，相反的情况被称为"曝光不足（欠曝、过暗）"。即使处于恰当曝光范
围内，有时候部分图像会因非常明亮而失去层次，这种现象被称为"高光溢出"，如果部分图像变为全黑则被称为"暗部缺失"。当整体曝光发生偏差时更容易出现
这样的现象。所以在拍摄时应始终保持采用合适的曝光。在理解了恰当曝光的基础上，再尝试有意识地使画面更亮（高调）或更暗（低调）的表现手法。</p>
