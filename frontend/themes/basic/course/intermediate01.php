                        <p class="pb20">就像前面说过的一样，所谓的曝光是用于表示照片整体亮度的术语。照片
的亮度基本是由图像感应器所接收到的光的总量决定，而光圈和快门就像调节阀，起到了调整光量的作用。快门的单位是时间，相当于光线之门打开的时间。而光圈
则表示光量调节阀打开的大小。可分别通过对两者进行调节来控制光线通过量。为了获得合适的亮度，需要对两者进行联动调节，可采用高速快门配合大光圈以得到
一定亮度，同样也可采用低速快门配合小光圈来获得同样的亮度。从曝光这个角度来说，我们可以认为这两种做法的结果是相同的。用跑马拉松来比喻的话，不管跑
得快与慢，只要到达终点，所跑距离是一样的。</p>
