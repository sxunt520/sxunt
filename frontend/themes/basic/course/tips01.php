                            <h5 class="pb10">要点5:抑制相机动作产生的振动</h5>
                            <p class="size13 pb20">特别是在使用远摄镜头拍摄时，连相机内反光镜
的动作也会成为画面抖动的原因。要抑制反光镜动作的话，推荐利用实时显示拍摄。因为实时显示拍摄状态下反光镜已经升起，没有弹起的振动。除此之外，使反光
镜动作安静的静音模式和可先升起反光镜再拍摄的反光镜预升功能也很有效。还可以使用快门线，这样就不会在按下快门按钮的时候带来振动。</p>
