                <h5 class="pb10">偶然的邂逅加上一定的技术拍摄出让人印象深刻的照片</h5>
                <p class="pb10">拍摄风景照片和在一张空白画布上绘画有异曲同工之妙。眼前是一整片壮丽的景色，但是如
何截取，如何表现就是创作者本人的自由了。决定画面构图之后，再使用必要的拍摄技巧就能拍摄出和构思一致的照片了。和绝美的景色邂逅有可能只是一种偶然，
但将其忠实记录在照片上则需要冷静的判断能力。在拍摄风景时，除了选择合适的参数设置进行拍摄以外，选择合适的拍摄时间也非常重要。一天中因为光线不断变
化，同样的风景在不同时间拍摄，效果会不同。要抓住一天中特定的时段才能拍下美丽的风景。如果是不能再来第二次的地方就没有办法了。但如果日程相对较为宽
松的话，不光可以白天拍，光线斜射的清晨或傍晚也可以来拍。那些让人印象深刻的风景照片有很多都是在此时段拍摄的。拍摄本身并不需要太复杂的技术，但是选
择合适的拍摄模式还是非常重要的，特别是在想把远处的风景拍得非常锐利时，就应该选择能发挥镜头分辨力的光圈，充分发挥相机的成像力。常使用的拍摄模式就
是Av（光圈优先自动曝光）模式。因为拍摄者可以自由选择光圈，与那些由相机自动设置光圈的拍摄模式相比，它留给了拍摄者更多自由选择的权利，所以很受用
户欢迎。虽然不同镜头的性能各不相同，但是想将远景拍摄得比较锐利时，一般来说将镜头的光圈设置在F5.6-8之间比较能发挥出镜头的分辨力。拍摄者可以
首先确定光圈，再考虑快门速度来进行拍摄。另外，能否获得锐利表现也受合焦状况的很大影响。推荐使用三脚架，选择手动对焦进行实时显示拍摄。</p>
