                <h5 class="pb10">了解白平衡的用途并熟练应用</h5>
                <p class="pb10">与胶片相机不同，数码相机不管在何种光源下都能以正确的色调进行拍摄，而白平衡就是用
于实现色调调节的。白平衡的基本概念是"在任何光源下都能将白色物体还原为白色"，对在特定光源下拍摄时出现的偏色现象，通过加强对应的补色来进行补偿。
上面的照片均是在日光下拍摄，仅改变了白平衡设置。各白平衡下的照片产生的偏色是所补偿光源的补色。使用胶片相机时，为了对这些偏色进行补偿，拍摄时要用
彩色滤镜。数码相机的基本原理与其类似，白平衡功能就相当于彩色滤镜。但在彩色滤镜中没有类似"自动白平衡"的滤镜，在这点上两者有很大区别。一般使用时
选择AWB（自动白平衡）就足够了，但在特定条件下如果色调不理想，可以选择使用其他的白平衡选项。</p>
