                <p>所谓的抖动是在相机的快门速度比被摄体或相机抖动速度低时发生的现象。由于被摄体的运动产生的抖动被称作"被摄体
抖动"。而当被摄体是风景等静止物体时，由相机摇晃产生的抖动被称为"手抖动"或者"相机抖动"。虽然这些现象在画面中都表现为被摄体模糊，但由于产生原
因不一样，需要将两者区别对待。对于这些抖动，有效的对策就是提高快门速度。为了提高快门速度我们可以使用高ISO感光度或选择大光圈镜头。但是，使用大
光圈镜头有时也不能完全消除抖动。使用搭载IS影像稳定器具备防抖功能的镜头对于防止手抖动非常有效，但即使是使用搭载IS影像稳定器的镜头也并不能防止
被摄专题体抖动，而只能对手抖动起到一定的效果。还有，抖动给画面带来的不一定都是负面影响，它对于展现被摄体的动感非常有用。在拍摄运动时，如有意识地
使运动员的手、脚等身体的某一部分因运动而产生模糊，就能拍出更有冲击力的照片。综上所述，能够同时灵活运用抖动与静止这两者对照片表现来说是非常重要的
因素。但是，原则上来说，可以容忍的是被摄体的抖动，而手抖动只要不是有意运用，应当尽可能避免其发生。所以，拍摄时建议运用防抖机构，在抑制手抖动不利
影响的同时掌握灵活运用被摄体抖动的高级拍摄技巧。</p>
