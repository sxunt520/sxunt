                <h5 class="pb10">快门速度不同被摄体的表现方式也不同</h5>
                <p class="pb10">简单说的话，快门速度表示光线照射图像感应器的时间长短。根据快门结构的不同，其动作
及系统也有很大差异。数码单反相机所采用的快门形式为焦平面快门，通过2片具有遮光性的快门帘幕的动作来调节曝光时间。在成像方面，当快门速度提高时，可
以将高速运动的被摄体定格于画面，被摄体运动之所以能够定格于画面，是因为在图像感应器曝光时，快门速度比被摄体的运动速度更快。而当快门速度降低时，将
产生被摄体抖动。被摄体抖动是因快门速度相对于被摄体的运动速度过低所产生的现象。快门速度在影响被摄体动作的表现形态的同时，还通过控制图像感应器受光
时间长短来精确控制曝光量。当图像感应器表面受光一定，即曝光相同时，如果快门的开放时间延长了，需要相应缩小光圈，相反，当采用高速快门时，应打开光圈
以便获得更多光量。快门速度与光圈相互之间有着密不可分的关系。快门速度对照片最终效果有着非常重要的影响。</p>
