自动，就能自如应对光线的急剧变化。白天在室外拍摄时，即使使用高ISO感光度拍摄，噪点也不会像在夜间拍摄时那么明显。因此在拍摄时应该随时注意快门速
度的变化，以防止可能出现的手抖动或者被摄体抖动。</p>
