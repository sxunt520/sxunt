                    	<h5 class="pb10">迅速反应，捕捉瞬间动态</h5>
                        <p class="pb10">与小型数码相机相比，数码单反相机的一个优势就是整个动作过程的时滞
较短。小型数码相机通常是通过背面液晶监视器进行观察后拍摄，从严格意义上讲，所拍摄到的画面并不是按下快门按钮那一瞬间的画面，而是略微滞后的画面，这
是因为液晶监视器上观察到的画面是实际影像转换成电子信号后生成的，所以会产生时滞。而数码单反相机是通过光学取景器来观察实际图像，所以不会产生电子方
面的时间差，当前所观察到的图像与被摄体的实际动作并无差异。而且不管是快门等的机械结构还是自动对焦功能的速度，数码单反相机都占有速度优势，具有不会
错失瞬间快门机会的高性能。即使是小型数码相机很难拍到的运动场景或人物的瞬间表情，数码单反相机都能够轻松捕捉。</p>
