                <p class="pb10">自动曝光模式是由相机自动判断快门速度和光圈，以求得到理想曝光效果的拍摄模式。由拍
摄者决定光圈，相机决定快门速度的模式叫"Av（光圈优先自动曝光）"，与之相反的叫做"Tv（快门优先自动曝光）"。而被称为"P（程序自动曝光）"的
模式可由相机同时决定快门速度和光圈，采用了高级算法来确定各设置值。之所以有多种曝光模式，是由于它们各自不同的功能可分别应用于多种不同的场景。在
Av模式下，由于拍摄者可以选择光圈，因此更容易控制背景虚化效果。而Tv模式则具有易于表现被摄体动与静的优点。光圈与快门不仅具有调节光量的功能，与
照片的表现效果也有着密切联系，所以相机会准备多种曝光模式供大家选择。</p>
