                <h5 class="pb10">注意光线朝向和阴影寻找能反映花卉之美的位置</h5>
                <p class="pb10">不论是谁拿起相机都想把花卉拍摄得更加美丽。但是拍摄花卉其实有一定难度，调整合焦位
置和虚化，还要选择光线等，这一切都要同时进行。拍摄花卉时首先需要决定的就是光源的位置。在大多数拍摄场景中，一般来说比较常见的是顺光拍摄，也就是太
阳位于拍摄者身后。但是对于需近拍的花卉摄影，这样的位置关系会导致花卉完全被拍摄者本身的阴影所笼罩。因此在拍摄花卉时，最好选择正午时垂直射下来的
光、逆光或半逆光来进行拍摄。在拍摄花卉时确定光线的位置非常重要，之后还需根据阴影的位置来确定拍摄位置和角度。拍摄模式可根据个人喜好来选择，如果想
要灵活利用背景虚化的话，使用Av（光圈优先自动曝光）模式将会比较方便。需要注意的是在微距拍摄时拍摄距离较近，不应过度虚化，如果虚化得过度就会导致
被摄体本身变得不明确。要收缩光圈以保持一定的景深，使花卉整体形状明确，只利用虚化来滤除杂乱的背景。此外，手动对焦比自动对焦更适合此类拍摄。合焦需
要精确到毫米的程度，所以说用人眼来确认更保险。而且有时自动对焦点和拍摄者想对焦的位置不一定相同。而微距拍摄时使用自动对焦锁定，对焦不准的可能性也
很大，所以在这里并不推荐使用。建议采用<a href="intermediate18.html" class="uline">中级篇专题</a>介绍的实时显示拍摄手动对焦的方法。室外拍摄时往往会有风，这会给手动对焦带来一定困难，但是使用这种方式拍摄到精确对焦的漂亮花卉照片时，会给拍摄者带来更加特别的感动。</p>
