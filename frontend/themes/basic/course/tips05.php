                <h5 class="pb10">拍摄夜景的理想时机往往比预想的来得更早</h5>
                <p class="pb10">日落后的美丽夜景是颇具魅力的被摄体。但是拍下的画面却总是不理想，和肉眼实际看到的
画面相去甚远。拍摄夜景的要点就在于拍摄时间的选择。可以说照片好坏的80%取决于是否在对的时间进行拍摄。拍摄夜景和用肉眼观赏夜景的好时机并不同。拍
摄夜景照片时的理想时机并不是在真正的夜晚，而是在天空还留有少许天光的傍晚。在这个时间，大厦的轮廓还没有完全消失在夜色里，而且那种蓝白色的光将会使
天空的色调看上去就像在梦幻中一样。如果等天完全黑了再进行拍摄，照片上发光的就只有点光源，看上去并不漂亮。推荐在傍晚到达拍摄现场，趁天色还明亮时观
察周围状况。特别应该注意暗下来之后拍摄现场的安全性以及很容易被忽视的可能影响拍摄的大型广告牌。拍摄应该在天空尚有余光的阶段开始，根据光线情况的变
化调整设置。使用Av（光圈优先自动曝光）模式，选择F5.6-8左右的光圈，这样的光圈有利于提高分辨力，拍下清晰的点光源。此外，在天空还亮的时候就
应调整好相机，确定画面水平，天色暗下来之后就很难看清那些用于确认是否水平的参照物，也就很难确定正确的角度了。使用水准仪也是不错的选择，但是周围变
暗后就很难进行观察，所以为了之后的拍摄能顺利进行，最好事先调整画面到水平。适合拍摄的时间段最长也不过5分钟，所以需要在理想时机到来之前做好一切准
备。</p>
